context("rcount")

test_that("rcount", {
  set.seed(101)
  expect_identical(rcount(), 799289926L)
  set.seed(101)
  expect_identical(rcount(), 799289926L)
  set.seed(101)
  expect_identical(rcount(2L), c(799289926L,  94113074L))
  set.seed(101)
  expect_identical(rcount(2L), c(799289926L,  94113074L))
  set.seed(102)
  expect_identical(rcount(2L), c(1227563851L, 1058457242L))
  set.seed(101)
  expect_identical(rcount(2L), c(799289926L,  94113074L))
  set.seed(101)
  expect_identical(rcount(0L), integer(0))
})
